package br.ufmg.hc.telessaude.webservices.mobile.entidades;

import java.util.Date;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

/**
 *
 * @author breno.melo
 */
@Entity
@Table(name = "visualizacao_alerta", schema = "prosind")
public class VisualizacaoAlerta {

    private static final long SerialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    @Temporal(TemporalType.TIMESTAMP)
    @Column(columnDefinition = "timestamp default current_timestamp")
    private Date inclusao;
    private Boolean naoVerNovamente;
    @JoinColumn(name = "id_usuario")
    @ManyToOne(optional = true)
    private Usuario destinatario;
    @JoinColumn(name = "id_alerta")
    @ManyToOne(optional = true)
    private Alerta alerta;

    public VisualizacaoAlerta() {
    }

    public VisualizacaoAlerta(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Object getClone() {
        try {
            return (super.clone());
        } catch (CloneNotSupportedException e) {
            return (null);
        }
    }

    public Usuario getDestinatario() {
        return destinatario;
    }

    public void setDestinatario(Usuario destinatario) {
        this.destinatario = destinatario;
    }

    public Date getInclusao() {
        return inclusao;
    }

    public void setInclusao(Date inclusao) {
        this.inclusao = inclusao;
    }

    public Boolean isNaoVerNovamente() {
        return naoVerNovamente;
    }

    public void setNaoVerNovamente(Boolean naoVerNovamente) {
        this.naoVerNovamente = naoVerNovamente;
    }

    public Alerta getAlerta() {
        return alerta;
    }

    public void setAlerta(Alerta alerta) {
        this.alerta = alerta;
    }
}
